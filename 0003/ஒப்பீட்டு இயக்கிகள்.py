# உதவிக்குறிப்புகள்:
# ==    -->  சமம்மா?
# !=     -->  சமம்மில்லையா?
# <=    --> குறைவாகவோ அல்லது சமமாகவோ உள்ளதா?
# > =   --> அதிகமாகவோ அல்லது சமமாகவோ உள்ளதா?
# <      --> ஐ விட குறைவா?
# >      --> ஐ விட பெரியதா?
# <      --> ஐ விட குறைவா?
